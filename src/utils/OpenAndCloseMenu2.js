// Cachorro
export function menu2CachorroAbrir() {
  let menu2Cachorro = document.querySelector('.menu2-cachorro');
  menu2Cachorro.classList.add('ativo');
}

export function menu2CachorroFechar() {
  let menu2Cachorro = document.querySelector('.menu2-cachorro');
  menu2Cachorro.classList.remove('ativo');
}

